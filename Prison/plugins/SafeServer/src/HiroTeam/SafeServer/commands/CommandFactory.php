namespace HiroTeam\SafeServer\commands;

use HiroTeam\SafeServer\SafeServerMain;

class CommandFactory
{
    /**
     * @var SafeServerMain
     */
    private $main;

    /**
     * CommandFactory constructor.
     * @param SafeServerMain $main
     */
    public function __construct(SafeServerMain $main)
    {
        $this->main = $main;
        $this->init();
    }

    private function init()
    {
        $this->main->getServer()->getCommandMap()->registerAll('SafeServer', [
            new SafeDelete($this->main)
        ]);
    }
}
